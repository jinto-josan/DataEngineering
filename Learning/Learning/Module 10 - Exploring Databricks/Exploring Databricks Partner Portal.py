# MAGIC ## Databricks Partner Portal
# MAGIC
# MAGIC In this lesson, we would like to formally introduce `Databricks Partner Portal` to you. We will help you sign in to the portal, provide a few options to explore it and optionally do an accreditation to proceed on `Lakehouse Fundamentals Learning Path`.
# MAGIC
# MAGIC ## Learning Objectives
# MAGIC By the end of this lesson, you should be able to:
# MAGIC * Sign in to Databricks Partner Portal and begin to explore it.
# MAGIC * Optionally complete an Apache Spark Transformations sourse.
# MAGIC * Optionally, complete a Lakehouse Fundamentals Course and get Accredited from Databricks.
# MAGIC
# MAGIC
# MAGIC 🕒 **Estimated Time For Completion:** 120 minutes
# MAGIC
# MAGIC Let us start with signing in to the databricks partner portal. Please click on this [link](https://partner-academy.databricks.com/learn) (you will see a `SIGN IN` button on the top right corner of the page. Clicking on this button will take you to the SSO Log in page. Please provide your Thoughtworks e-mail id here and submit. A code will come to your Thoughtworks mailbox which will help you log in to the databricks partner portal)
# MAGIC
# MAGIC Once signed in, you have 3 options to explore as listed below. Do go through all of them or at least one if you are short of time. We have also listed down a few pros and cons of the options below if you really have time to complete only one option
# MAGIC
# MAGIC ### Option 1 - Databricks Accreditation
# MAGIC
# MAGIC In this option, we will help you complete the `Databricks Fundamentals` course and receive the `Databricks Fundamentals Accreditation` from Databricks.
# MAGIC
# MAGIC - Please click on this [link](https://partner-academy.databricks.com/learn/lp/215/Databricks%2520Fundamentals%2520Learning%2520Plan) to open the `Databricks Fundamentals Learning Path`. You should be able to see `Databrick Fundamentals` course and `Databricks Fundamental Accreditation` mentioned in the `Courses in the Learning Plan` section at the bottom.
# MAGIC
# MAGIC - Click on the `Databrick Fundamentals` course, enroll in it and complete it. The course has only videos with no hands-on and would take about an hour to complete.
# MAGIC
# MAGIC - Once done, click on `Databricks Fundamental Accreditation` on the same page and complete the test. The test should take you about 30 mins to complete.
# MAGIC
# MAGIC **Pros of exploring this option**
# MAGIC - You get a databricks badge!
# MAGIC - Better understanding of the Databricks product landscape
# MAGIC
# MAGIC **Cons of exploring this option**
# MAGIC - It is quite boring to watch the videos, especially at 1x speed.
# MAGIC - In order to clear the accreditation exam, you might have to do additional googling.
# MAGIC
# MAGIC ### Option 2 - Transforming Data with Spark
# MAGIC
# MAGIC Apache Spark has been an integral part of our course so far. So it will be a good idea to (optionally) learn a bit more about some spark transformations. The course at this [link](https://partner-academy.databricks.com/learn/course/internal/view/elearning/1878/transform-data-with-spark) does exactly that.
# MAGIC
# MAGIC **Pros of exploring this option**
# MAGIC - You will learn a bit more about Spark Transformations like repartition, partitionBy etc
# MAGIC
# MAGIC **Cons of exploring this option**
# MAGIC - You might find it quite familiar, especially after completing so many notebooks.
# MAGIC - Also, the Spark video referred in a learners guide (Vanilla Spark) covers most of it.
# MAGIC
# MAGIC
# MAGIC ### Option 3 - Databricks Portal Courses
# MAGIC
# MAGIC In this option, we would like to invite you to explore some free short courses from Databricks available at this [link](https://partner-academy.databricks.com/learn/catalog?ctldoc-catalog-0=t-_elearning~du-_0_10320). Please feel free to choose any course that you like and attempt the accompanying accreditation if provided with the course of your choice.
# MAGIC
# MAGIC **Pros of exploring this option**
# MAGIC - Some additional learnings
# MAGIC - Look out for courses on AI since it's quite a popular area these days.
# MAGIC
# MAGIC **Cons of exploring this option**
# MAGIC - You might have to put in some extra effort outside the recommended time for these options.
