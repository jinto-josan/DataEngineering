# MAGIC ## How does it work, at a glance:
# MAGIC
# MAGIC - EV plugs into a Charge Point
# MAGIC - Charge Point connects to and authenticates with a CSMS using Websockets and the OCPP Protocol
# MAGIC - The Charge Point sends a message to the CSMS according to the OCPP protocol
# MAGIC - The CSMS receives the message, processes/records it, and sends back a positive/negative response
# MAGIC
# MAGIC The OCPP Protocol is an open standard created by the Open Charge Alliance (OCA) as an effort to standardise communication and encourage interoperability. Both Charge Points (the actual devices) and CSMSs can be certified by the OCA as being compliant with the OCPP Protocol. Despite that, there seems to be some variation between implementations of both Charge Points and CSMSs which are the result of interpretations of OCPP. There is an official testing protocol on the path to certification - both testing and access to the User Acceptance Test (UAT) document provided as part of their OCTT (OCPP Testing Tool) require OCA membership and vetting by OCA. Every year, the OCA has an event called PlugFest where CSMS developers can come to a single place to test their implementations against various Charge Points.
# MAGIC
# MAGIC The OCPP Protocol, at the time of writing have the following versions:
# MAGIC
# MAGIC - OCPP 1.5 - deemed out of date but many Charge Points still only support this (they're recommended to update to at least the next version)
# MAGIC - [OCPP 1.6](https://www.openchargealliance.org/protocols/ocpp-16/) - most widely implemented, supports both SOAP/JSON versions
# MAGIC - [OCPP 2.0.1](https://www.openchargealliance.org/protocols/ocpp-201/) - improvements made in security and smart charging, not all Charge Points and CSMS implement this but are encouraged to do so to stay competitive.
# MAGIC
# MAGIC Staying true to versioning for APIs, version 2.0.1 is not backwards compatible with version 1.6, but they do have some core functionalities that they share.
